<?php

namespace App\Entity;

use App\Repository\CategorieRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;


#[ORM\Entity(repositoryClass: CategorieRepository::class)]
class Categorie
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column(length: 250)]
    #[Assert\NotBlank(message: "Please enter a title.")]
    #[Assert\Length(min: 2, max: 30, minMessage: "Le type doit comporter au moins {{ limit }} caractères",    maxMessage: "Le nom ne peut pas dépasser {{ limit }} caractères")]

    private ?string $nom = null;

    #[ORM\Column(length: 250)]
    #[Assert\NotBlank(message: "Please enter a featured Text")]
    #[Assert\Length(min: 2, max: 30, minMessage: "Le type doit comporter au moins {{ limit }} caractères",    maxMessage: "Le type ne peut pas dépasser {{ limit }} caractères")]


    private ?string $Type = null;



    #[ORM\OneToMany(mappedBy: 'categorie', targetEntity: Evenement::class)]
    private Collection $evenements;

    #[ORM\Column(nullable: true)]
    private ?bool $archive = null;

    public function __construct()
    {
        $this->evenements = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getNom(): ?string
    {
        return $this->nom;
    }

    public function setNom(string $nom): self
    {
        $this->nom = $nom;

        return $this;
    }

    public function getType(): ?string
    {
        return $this->Type;
    }

    public function setType(string $Type): self
    {
        $this->Type = $Type;

        return $this;
    }




    /**
     * @return Collection<int, Evenement>
     */
    public function getEvenements(): Collection
    {
        return $this->evenements;
    }

    public function addEvenement(Evenement $evenement): self
    {
        if (!$this->evenements->contains($evenement)) {
            $this->evenements->add($evenement);
            $evenement->setCategorie($this);
        }

        return $this;
    }

    public function removeEvenement(Evenement $evenement): self
    {
        if ($this->evenements->removeElement($evenement)) {
            // set the owning side to null (unless already changed)
            if ($evenement->getCategorie() === $this) {
                $evenement->setCategorie(null);
            }
        }

        return $this;
    }

    public function isArchive(): ?bool
    {
        return $this->archive;
    }

    public function setArchive(?bool $archive): self
    {
        $this->archive = $archive;

        return $this;
    }
}
